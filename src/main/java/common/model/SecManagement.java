package common.model;

public class SecManagement {
	private String Name;
	private Integer Video;
	private Integer Ware;
	private Integer Door;
	private Integer Grid;
	private Integer Meeting;
	private Integer Living;
	private Integer Shield;
	private String Code;
	
	
	public String getCode() {
		return Code;
	}
	public void setCode(String code) {
		Code = code;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public Integer getVideo() {
		return Video;
	}
	public void setVideo(Integer video) {
		Video = video;
	}
	public Integer getWare() {
		return Ware;
	}
	public void setWare(Integer ware) {
		Ware = ware;
	}
	public Integer getDoor() {
		return Door;
	}
	public void setDoor(Integer door) {
		Door = door;
	}
	public Integer getGrid() {
		return Grid;
	}
	public void setGrid(Integer grid) {
		Grid = grid;
	}
	public Integer getMeeting() {
		return Meeting;
	}
	public void setMeeting(Integer meeting) {
		Meeting = meeting;
	}
	public Integer getLiving() {
		return Living;
	}
	public void setLiving(Integer living) {
		Living = living;
	}
	public Integer getShield() {
		return Shield;
	}
	public void setShield(Integer shield) {
		Shield = shield;
	}
}
